    public void tampil() {
        String sql="select inventaris_produsen.kode_produsen, inventaris_produsen.nama_produsen, inventaris_produsen.alamat_produsen, inventaris_produsen.no_telp,"+
                   "inventaris_produsen.email, inventaris_produsen.website_produsen from inventaris_produsen "+
                   "where inventaris_produsen.kode_produsen like '%"+TCari.getText().trim()+"%' "+
                    "or inventaris_produsen.nama_produsen like '%"+TCari.getText().trim()+"%' "+
                    "or inventaris_produsen.alamat_produsen like '%"+TCari.getText().trim()+"%' "+
                    "or inventaris_produsen.no_telp like '%"+TCari.getText().trim()+"%' "+
                    "or inventaris_produsen.email like '%"+TCari.getText().trim()+"%' "+
                    "or inventaris_produsen.website_produsen like '%"+TCari.getText().trim()+"%' order by inventaris_produsen.kode_produsen";
        prosesCari(sql);
    }

    private void prosesCari(String sql) {
        Valid.tabelKosong(tabMode);
        try{
            ResultSet rs=koneksiDB.condb().prepareStatement(sql).executeQuery();
            while(rs.next()){
                String[] data={rs.getString(1),
                               rs.getString(2),
                               rs.getString(3),
                               rs.getString(4),
                               rs.getString(5),
                               rs.getString(6)};
                tabMode.addRow(data);
            }
        }catch(SQLException e){
            System.out.println("Notifikasi : "+e);
        }
        LCount.setText(""+tabMode.getRowCount());
    }

