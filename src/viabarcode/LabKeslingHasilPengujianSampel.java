    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        jml=0;
        for(i=0;i<tbPengujian.getRowCount();i++){
            if(tbPengujian.getValueAt(i,0).toString().equals("true")){
                jml++;
            }
        }
        if(KdAnalis.getText().equals("")||NmAnalis.getText().equals("")){
            Valid.textKosong(TCariPeriksa,"Petugas Pengujian/Analis");
        }else if(KodePelanggan.getText().equals("")||NamaPelanggan.getText().equals("")){
            Valid.textKosong(TCariPeriksa,"Pelanggan");
        }else if(TNoPermintaan.getText().equals("")){
            Valid.textKosong(TNoPermintaan,"Nomor Permintaan");
        }else if(TNoPenugasan.getText().equals("")){
            Valid.textKosong(TNoPenugasan,"Nomor Penugasan");
        }else if(KodeSampel.getText().equals("")||NamaSampel.getText().equals("")){
            Valid.textKosong(TCariPeriksa,"Sampel");
        }else if(tabMode.getRowCount()==0){
            Valid.textKosong(TCariPeriksa,"Data Permintaan");
        }else if(jml==0){
            Valid.textKosong(TCariPeriksa,"Data Permintaan");
        }else{
            int reply = JOptionPane.showConfirmDialog(rootPane,"Eeiiiiiits, udah bener belum data yang mau disimpan..??","Konfirmasi",JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
                ChkJln.setSelected(false);
                Sequel.AutoComitFalse();
                berhasil=true;
                
                for(i=0;i<tbPengujian.getRowCount();i++){ 
                    if(tbPengujian.getValueAt(i,0).toString().equals("true")){
                        if(Sequel.menyimpantf2("laborat_kesling_hasil_pengujian_sampel","?,?,?,?,?","Hasil Pengujian",5,new String[]{
                            TNoPenugasan.getText(),tbPengujian.getValueAt(i,1).toString(),tbPengujian.getValueAt(i,7).toString(),Valid.SetTgl(TanggalPenugasan.getSelectedItem()+"")+" "+CmbJam.getSelectedItem()+":"+CmbMenit.getSelectedItem()+":"+CmbDetik.getSelectedItem(),"Belum Diverifikasi"
                        })==false){
                            berhasil=false;
                        }
                    }                    
                }
                
                if(berhasil==true){
                    Sequel.queryu("update laborat_kesling_penugasan_pengujian_sampel set status='Sudah Keluar Hasil' where no_penugasan='"+TNoPenugasan.getText()+"'");
                    Sequel.Commit();
                    JOptionPane.showMessageDialog(null,"Proses simpan selesai...!");
                    for(i=0;i<tbPengujian.getRowCount();i++){
                        tbPengujian.setValueAt(false,i,0);
                        tbPengujian.setValueAt("",i,7);
                    }
                }else{
                    JOptionPane.showMessageDialog(null,"Terjadi kesalahan saat pemrosesan data, transaksi dibatalkan.\nPeriksa kembali data sebelum melanjutkan menyimpan..!!");
                    Sequel.RollBack();
                }
                
                Sequel.AutoComitTrue();    
                ChkJln.setSelected(true);  
                this.setCursor(Cursor.getDefaultCursor());
            }  
        }
    }//GEN-LAST:event_BtnSimpanActionPerformed

