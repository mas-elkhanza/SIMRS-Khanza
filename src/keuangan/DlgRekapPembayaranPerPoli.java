    private void prosesCari() {            
        try{   
            this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR)); 
            Valid.tabelKosong(tabMode);
            pspoli=koneksi.prepareStatement("select kd_poli,nm_poli from poliklinik where nm_poli like ?");
            try {
                pspoli.setString(1,"%"+TCari.getText().trim()+"%"); 
                rspoli=pspoli.executeQuery();
                i=1;
                ttljmlpas=0;
                ttltotal=0;
                while(rspoli.next()){
                    psmasuk=koneksi.prepareStatement(
                            "select sum(besar_bayar),count(DISTINCT reg_periksa.no_rawat) from detail_nota_jalan inner join reg_periksa "+
                            "on reg_periksa.no_rawat=detail_nota_jalan.no_rawat where "+
                            "reg_periksa.kd_poli=? and reg_periksa.status_lanjut='Ralan' and "+
                            "reg_periksa.no_rawat not in (select piutang_pasien.no_rawat "+
                            "from piutang_pasien where piutang_pasien.no_rawat=reg_periksa.no_rawat) "+
                            "and reg_periksa.tgl_registrasi between ? and ? ");
                    try {
                        psmasuk.setString(1,rspoli.getString(1));
                        psmasuk.setString(2,Valid.SetTgl(Tgl1.getSelectedItem()+""));
                        psmasuk.setString(3,Valid.SetTgl(Tgl2.getSelectedItem()+""));
                        rsmasuk=psmasuk.executeQuery();
                        jmlpas=0;
                        total=0;
                        while(rsmasuk.next()){
                            jmlpas=rsmasuk.getInt(2);
                            total=rsmasuk.getDouble(1);
                            ttljmlpas=ttljmlpas+rsmasuk.getInt(2);
                            ttltotal=ttltotal+rsmasuk.getDouble(1);
                        }                        
                    } catch (Exception e) {
                        System.out.println("Notif 2 : "+e);
                    } finally{
                        if(rsmasuk!=null){
                            rsmasuk.close();
                        }
                        if(psmasuk!=null){
                            psmasuk.close();
                        }
                    }
                    tabMode.addRow(new Object[]{i+". ",rspoli.getString(2),jmlpas,total});
                    i++;
                }
            } catch (Exception e) {
                System.out.println(e);
            } finally{
                if(rspoli!=null){
                    rspoli.close();
                }
                if(pspoli!=null){
                    pspoli.close();
                }
            } 
            tabMode.addRow(new Object[]{"","Total :",ttljmlpas,ttltotal});
            this.setCursor(Cursor.getDefaultCursor());             
        }catch(Exception e){
            System.out.println("Catatan  "+e);
        }
        
    }
    
    
        
    
    
