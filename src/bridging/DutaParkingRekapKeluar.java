    private void prosesCari() {
        this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        try {
            StringBuilder htmlContent = new StringBuilder();
            htmlContent.append("<tr class='isi'>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Kode</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>No.Polisi</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Masuk</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Keluar</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Durasi</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Pintu Masuk</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Pintu Keluar</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Jenis Kendaraan</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Tarif</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Parkir</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Inap</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Denda</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Bermasalah</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Paket</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Kadaluarsa</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>RFID</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Petugas</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Shift</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Waktu Login</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Waktu Logout</td>").
                            append("<td valign='middle' bgcolor='#FFFAFA' align='center'>Tipe</td>").
                        append("</tr>");  
            koneksi=koneksiDBDutaParking.condb();
            ps=koneksi.prepareStatement(
                    "select keluar.kode,keluar.nopol,keluar.masuk,keluar.keluar,keluar.durasi,keluar.pintum,keluar.pintuk,keluar.jenisk,keluar.tarif,"+
                    "keluar.parkir,keluar.inap,keluar.denda,keluar.tiketmasalah,keluar.paket,keluar.kadaluarsa,keluar.rfid,keluar.petugas,keluar.shif,"+
                    "keluar.wktlogin,keluar.wktlogout,keluar.tipe from keluar where keluar.masuk between ? and ? "+
                    (TCari.getText().trim().equals("")?"":"and (keluar.kode like ? or keluar.nopol like ? or keluar.jenisk like ? or keluar.paket like ? or "+
                    "keluar.rfid like ? or keluar.petugas like ? or keluar.shif like ? or keluar.tipe like ?) ")+"order by keluar.masuk");
            try {
                ps.setString(1,Valid.SetTgl(Tgl1.getSelectedItem()+"")+" 00:00:00");
                ps.setString(2,Valid.SetTgl(Tgl2.getSelectedItem()+"")+" 23:59:59");
                if(!TCari.getText().trim().equals("")){
                    ps.setString(3,"%"+TCari.getText()+"%");
                    ps.setString(4,"%"+TCari.getText()+"%");
                    ps.setString(5,"%"+TCari.getText()+"%");
                    ps.setString(6,"%"+TCari.getText()+"%");
                    ps.setString(7,"%"+TCari.getText()+"%");
                    ps.setString(8,"%"+TCari.getText()+"%");
                    ps.setString(9,"%"+TCari.getText()+"%");
                    ps.setString(10,"%"+TCari.getText()+"%");
                }
                rs=ps.executeQuery();
                double totalparkir=0,totalinap=0,totaldenda=0,totalbermasalah=0;
                while(rs.next()){
                    totalparkir = totalparkir+rs.getDouble("parkir");
                    totalinap = totalinap+rs.getDouble("inap");
                    totaldenda = totaldenda+rs.getDouble("denda");
                    totalbermasalah = totalbermasalah+rs.getDouble("tiketmasalah");
                    htmlContent.append("<tr class='isi'>").
                                    append("<td align='center'>").append(rs.getString("kode")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("nopol")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("masuk")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("keluar")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("durasi")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("pintum")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("pintuk")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("jenisk")).append("</td>").
                                    append("<td align='center'>").append(Valid.SetAngka(rs.getDouble("tarif"))).append("</td>").
                                    append("<td align='center'>").append(Valid.SetAngka(rs.getDouble("parkir"))).append("</td>").
                                    append("<td align='center'>").append(Valid.SetAngka(rs.getDouble("inap"))).append("</td>").
                                    append("<td align='center'>").append(Valid.SetAngka(rs.getDouble("denda"))).append("</td>").
                                    append("<td align='center'>").append(Valid.SetAngka(rs.getDouble("tiketmasalah"))).append("</td>").
                                    append("<td align='center'>").append(rs.getString("paket")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("kadaluarsa")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("rfid")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("petugas")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("shif")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("wktlogin")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("wktlogout")).append("</td>").
                                    append("<td align='center'>").append(rs.getString("tipe")).append("</td>").
                                append("</tr>");
                }
                if((totalparkir>0)||(totalinap>0)||(totaldenda>0)||(totalbermasalah>0)){
                    htmlContent.append("<tr class='isi'>").
                                        append("<td align='right' colspan='9'>Total :</td>").
                                        append("<td align='center'>").append(Valid.SetAngka(totalparkir)).append("</td>").
                                        append("<td align='center'>").append(Valid.SetAngka(totalinap)).append("</td>").
                                        append("<td align='center'>").append(Valid.SetAngka(totaldenda)).append("</td>").
                                        append("<td align='center'>").append(Valid.SetAngka(totalbermasalah)).append("</td>").
                                    append("<td align='center' colspan='8'></td>").
                                    append("</tr>");
                }
            } catch (Exception e) {
                System.out.println("Notif : "+e);
            } finally{
                if(rs!=null){
                    rs.close();
                }
                if(ps!=null){
                    ps.close();
                }
            }
            
            LoadHTML.setText(
                    "<html>"+
                      "<table width='1500px' border='0' align='center' cellpadding='3px' cellspacing='0' class='tbl_form'>"+
                       htmlContent.toString()+
                      "</table>"+
                    "</html>");
            htmlContent=null;
        } catch (Exception e) {
            System.out.println("Notif : "+e);
        } 
        this.setCursor(Cursor.getDefaultCursor());
        
    }
    
