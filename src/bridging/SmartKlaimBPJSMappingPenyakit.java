    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        if(KodePenyakit.getText().trim().equals("")||NamaPenyakit.getText().trim().equals("")){
            Valid.textKosong(KodePenyakit,"Penyakit");
        }else if(KodeSnomed.getText().trim().equals("")){
            Valid.textKosong(KodeSnomed,"Kode Snomed");
        }else if(SnomedSystem.getText().trim().equals("")){
            Valid.textKosong(SnomedSystem,"System");
        }else if(Display.getText().trim().equals("")){
            Valid.textKosong(Display,"Display");
        }else{
            if(Sequel.menyimpantf("mapping_penyakit_smart_klaim_bpjs","?,?,?,?","Mapping",4,new String[]{
                KodeSnomed.getText(),Display.getText(),SnomedSystem.getText(),KodePenyakit.getText()
            })==true){
                tabMode.addRow(new Object[]{
                    KodeSnomed.getText(),Display.getText(),SnomedSystem.getText(),KodePenyakit.getText(),NamaPenyakit.getText()
                });
                emptTeks();
                LCount.setText(""+tabMode.getRowCount());
            }                
        }
}//GEN-LAST:event_BtnSimpanActionPerformed

