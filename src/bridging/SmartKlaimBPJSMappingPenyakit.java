    private void tampil() {
        Valid.tabelKosong(tabMode);
        try{
           ps=koneksi.prepareStatement(
                   "select mapping_penyakit_smart_klaim_bpjs.kode_snomed,mapping_penyakit_smart_klaim_bpjs.display,mapping_penyakit_smart_klaim_bpjs.system,"+
                   "mapping_penyakit_smart_klaim_bpjs.icd10,penyakit.nm_penyakit from mapping_penyakit_smart_klaim_bpjs inner join penyakit "+
                   "on mapping_penyakit_smart_klaim_bpjs.icd10=penyakit.kd_penyakit "+(TCari.getText().equals("")?"":"where mapping_penyakit_smart_klaim_bpjs.kode_snomed like ? "+
                   "or mapping_penyakit_smart_klaim_bpjs.display like ? or mapping_penyakit_smart_klaim_bpjs.icd10 like ? or penyakit.nm_penyakit like ? ")+
                   "order by mapping_penyakit_smart_klaim_bpjs.kode_snomed");
            try {
                if(!TCari.getText().equals("")){
                    ps.setString(1,"%"+TCari.getText()+"%");
                    ps.setString(2,"%"+TCari.getText()+"%");
                    ps.setString(3,"%"+TCari.getText()+"%");
                    ps.setString(4,"%"+TCari.getText()+"%");
                }
                rs=ps.executeQuery();
                while(rs.next()){
                    tabMode.addRow(new Object[]{
                        rs.getString("kode_snomed"),rs.getString("display"),rs.getString("system"),rs.getString("icd10"),rs.getString("nm_penyakit")
                    });
                }
            } catch (Exception e) {
                System.out.println("Notif : "+e);
            } finally{
                if(rs!=null){
                    rs.close();
                }
                if(ps!=null){
                    ps.close();
                }
            }
        }catch(Exception e){
            System.out.println("Notifikasi : "+e);
        }
        LCount.setText(""+tabMode.getRowCount());
    }

