    public void tampil(){        
        Valid.tabelKosong(tabMode);
        try{
            ps=koneksi.prepareStatement(
                "select count(diagnosa_pasien.no_rawat) as jumlah,diagnosa_pasien.kd_penyakit,penyakit.nm_penyakit "+
                "from penyakit inner join diagnosa_pasien inner join reg_periksa on diagnosa_pasien.kd_penyakit=penyakit.kd_penyakit "+
                "and reg_periksa.no_rawat=diagnosa_pasien.no_rawat where diagnosa_pasien.status='Ranap' and reg_periksa.tgl_registrasi between ? and ? "+
                "and reg_periksa.kd_pj like ? group by diagnosa_pasien.kd_penyakit order by diagnosa_pasien.kd_penyakit");
            try {
                ps.setString(1,Valid.SetTgl(Tgl1.getSelectedItem()+""));
                ps.setString(2,Valid.SetTgl(Tgl2.getSelectedItem()+""));
                ps.setString(3,"%"+kdpenjab.getText().trim()+"%");
                rs=ps.executeQuery();
                i=1;
                ttljmlpasien=0;
                while(rs.next()){
                    ttljmlpasien=ttljmlpasien+rs.getInt("jumlah");
                    tabMode.addRow(new Object[]{
                        i,rs.getString("kd_penyakit"),rs.getString("nm_penyakit"),rs.getString("jumlah")
                    });
                    i++;                     
                }
                if(ttljmlpasien>0){
                    tabMode.addRow(new Object[]{
                        "","Total","",ttljmlpasien
                    });
                }
            } catch (Exception e) {
                System.out.println("Notif : "+e);
            } finally{
                if(rs!=null){
                    rs.close();
                }
                if(ps!=null){
                    ps.close();
                }
            }                
        }catch(Exception e){
            System.out.println("Notifikasi : "+e);
        }
    }

