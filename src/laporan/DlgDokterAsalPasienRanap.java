    public void tampil(){        
        Valid.tabelKosong(tabMode);
        try{
            ps=koneksi.prepareStatement(
                    "select dokter.kd_dokter,dokter.nm_dokter from dokter where dokter.status='1' "+
                    (TCari.getText().trim().equals("")?"":"and (dokter.kd_dokter like ? or dokter.nm_dokter like ?) ")+
                    "order by dokter.kd_dokter");
            try {
                if(!TCari.getText().trim().equals("")){
                    ps.setString(1,"%"+TCari.getText().trim()+"%");
                    ps.setString(2,"%"+TCari.getText().trim()+"%");
                }
                rs=ps.executeQuery();
                int jumlahpasien,totaljumlahpasien=0;
                double jasadokter;
                while(rs.next()){
                    if(kdpenjab.getText().trim().equals("")){
                        jumlahpasien=Sequel.cariInteger("select count(reg_periksa.no_rawat) from reg_periksa where reg_periksa.status_lanjut='Ranap' and reg_periksa.tgl_registrasi between '"+Valid.SetTgl(Tgl1.getSelectedItem()+"")+"' and '"+Valid.SetTgl(Tgl2.getSelectedItem()+"")+"' and reg_periksa.kd_dokter='"+rs.getString("kd_dokter")+"'");
                    }else{
                        jumlahpasien=Sequel.cariInteger("select count(reg_periksa.no_rawat) from reg_periksa where reg_periksa.status_lanjut='Ranap' and reg_periksa.tgl_registrasi between '"+Valid.SetTgl(Tgl1.getSelectedItem()+"")+"' and '"+Valid.SetTgl(Tgl2.getSelectedItem()+"")+"' and reg_periksa.kd_dokter='"+rs.getString("kd_dokter")+"' and reg_periksa.kd_pj='"+kdpenjab.getText()+"'");
                    }
                    totaljumlahpasien=totaljumlahpasien+jumlahpasien;
                    jasadokter=nilaiperujuk*jumlahpasien;
                    tabMode.addRow(new Object[]{
                        rs.getString("kd_dokter"),rs.getString("nm_dokter"),jumlahpasien,Valid.SetAngka(jasadokter)
                    });
                }
                tabMode.addRow(new Object[]{
                    ">>","Jumlah Total Pasien",totaljumlahpasien,Valid.SetAngka(totaljumlahpasien*nilaiperujuk)
                });
            } catch (Exception e) {
                System.out.println("Notif : "+e);
            } finally{
                if(rs!=null){
                    rs.close();
                }
                if(ps!=null){
                    ps.close();
                }
            }   
        }catch(Exception e){
            System.out.println("Notifikasi : "+e);
        }
    }
